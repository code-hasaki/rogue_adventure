-module(action_become_idle_state).

%%--------------------------------------------------------------------
%% include
%%--------------------------------------------------------------------
-include("behavior3.hrl").
-include("rogue_adventure.hrl").
%%--------------------------------------------------------------------
%% export API
%%--------------------------------------------------------------------
-export([tick/3]).

%%--------------------------------------------------------------------
%% API functions
%%--------------------------------------------------------------------
-spec tick(tree_node(), blackboard(), term()) -> {bt_status(), blackboard(), term()}.
tick(_TreeNode, BB, #{misc := Misc} = State) ->
    UpState1 = State#{misc := maps:without(?IDLE_ERASE_MISC_KEY_LIST, Misc)},
    UpState2 = maps:without(?IDLE_DELETE_KEY_LIST, UpState1),
    {?BT_SUCCESS, BB, UpState2#{state := ?UNIT_STATE_IDLE}}.

