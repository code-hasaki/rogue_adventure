-spec tick(tree_node(), blackboard(), term()) -> {bt_status(), blackboard(), term()}.
tick(_TreeNode, BB, #{attr := #{cd := CD}} = State) ->
    case util:now_msec() >= CD of
        ?true ->
            {?BT_SUCCESS, BB, State};
        ?false ->
            {?BT_FAILURE, BB, State}
    end.

